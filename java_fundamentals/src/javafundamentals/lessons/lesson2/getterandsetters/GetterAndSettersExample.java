package javafundamentals.lessons.lesson2.getterandsetters;

public class GetterAndSettersExample {

    private int r;
    private double p;


    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }




}
